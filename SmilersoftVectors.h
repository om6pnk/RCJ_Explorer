class Vector2 {
	public:
		double x;
		double y;

	Vector2(double x, double y) {
		this->x = x;
		this->y = y;
	}
};

class Vector3 {
	public:
		double x;
		double y;
		double z;

	Vector3(double x, double y, double z) {
		this->x = x;
		this->y = y;
		this->z = z;
	}
};

class Vector4 {
	public:
		double x;
		double y;
		double z;
		double w;

	Vector4(double x, double y, double z, double w) {
		this->x = x;
		this->y = y;
		this->z = z;
		this->w = w;
	}
};